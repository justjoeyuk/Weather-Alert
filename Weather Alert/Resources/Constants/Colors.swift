import UIKit


extension UIColor {
    
    static func darkBackgroundColor() -> UIColor {
        return UIColor(red: 0.1, green: 0.1, blue: 0.1, alpha: 1)
    }
    
    static func darkTransparentColor() -> UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.4)
    }
    
    static func veryDarkTransparentColor() -> UIColor {
        return UIColor(red: 0, green: 0, blue: 0, alpha: 0.8)
    }
    
}